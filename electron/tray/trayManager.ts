  private tray: Tray | null = null
  private mainWindow: BrowserWindow | null = null

  /**
   * 创建托盘图标
   */
  create(mainWindow: BrowserWindow): Tray {
    this.mainWindow = mainWindow

    // 使用内联方式创建托盘图标（1x1 透明 PNG 作为 fallback，
    // 实际项目中应提供真实图标文件）
    const iconPath = join(__dirname, '../../resources/tray-icon.png')
    let trayIcon: ReturnType<typeof nativeImage.createEmpty>

    try {
      trayIcon = nativeImage.createFromPath(iconPath)
    } catch {
      // 创建空白图标作为 fallback
      const emptyIcon = nativeImage.createEmpty()
      trayIcon = emptyIcon
    }

    // 如果图标太小，创建一个简单的彩色图标
    if (trayIcon.isEmpty()) {
      trayIcon = this.createDefaultIcon()
    }

    // 确保图标尺寸适合托盘
    if (trayIcon.getSize().width > 32) {
      trayIcon = trayIcon.resize({ width: 16, height: 16 })
    }

    this.tray = new Tray(trayIcon)
    this.tray.setToolTip('Vibe Notch - Terminal Session Monitor')

    // 设置右键菜单
    this.updateContextMenu()

    // 左键点击切换显示/隐藏
    this.tray.on('click', () => {
      this.toggleWindow()
    })

    // 右键点击显示菜单
    this.tray.on('right-click', () => {
      this.tray?.popUpContextMenu()
    })

    return this.tray
  }

